    public void initDataBase(){
        try{
            myDB.initDataBase();
            this.sqlErrorLog = "";
            this.setVisible(true);
        }catch(MyException ex){
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error DB", JOptionPane.ERROR_MESSAGE);
            this.sqlErrorLog = ex.getMessage();
            try {
                if (!this.sqlErrorLog.equals("") || !this.sqlErrorLog.isEmpty()) {
                    GestorFiles.addLogSQL(this.sqlErrorLog);
                }
            } catch (MyException e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
            this.dispose();
        }
    }
    
