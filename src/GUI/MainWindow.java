    private void btnBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBajaActionPerformed
        String a = JOptionPane.showInputDialog("Introduce el codigo del articulo");
        
        try {
            String[] rs = myDB.buscaArticulo(a);
            int num = myDB.bajaArticulo(a);
            if (num > 0) {
                GestorFiles.addArticuloEliminado(rs);
            }
            if (num  == 1) {
                JOptionPane.showMessageDialog(this, "Se ha eliminado " + num + " articulo.");
            }else{
                JOptionPane.showMessageDialog(this, "No hemos encontrado ese codigo de articulos");
            }
        } catch (MyException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnBajaActionPerformed

